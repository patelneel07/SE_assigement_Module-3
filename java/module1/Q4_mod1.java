/*
  Write a program 
in Java to display the first 10 natural numbers using while 
loop. 
 */
package com.Module1;

public class Q4_mod1 
{
	public static void main(String[] args) 
	{
		int i = 1; 
        while (i <= 10) 
        {
            System.out.println(i);
            i++; 
        }
		
	}

}
